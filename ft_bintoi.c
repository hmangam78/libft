#include <stdio.h>

int	ft_bintoi(const char *string)
{
	int	num;
	int	i;

	num = 0;
	i = 0;
	while (i <= 7)
	{
		num = (2 * num) + (string[i] - '0');
		i++;
	}
	return (num);
}
